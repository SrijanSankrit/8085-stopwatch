# code for the start-stop-reset functions of stopwatch

#PAUSE ROUTINE
MVI A,1BH
SIM
EI
MOV A,B
CPI 00H
JNZ DOWN1
MVI B, 01H
SAME : JMP SAME

# START ROUTINE
DOWN1 : CPI 01H
JNZ DOWN2

#RESET ROUTINE
MVI B , 02H
MVI A,00H
CALL MODIDT
LXI H,0000H
CALL MODIAD
MVI B,02H
UP1 : MVI A,00H
LXI H,0000H
JMP UP1
MVI B,00H
MVI A,00H
DOWN2 : RETURN
